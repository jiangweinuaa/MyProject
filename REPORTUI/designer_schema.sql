-- 1. 报表定义表
CREATE TABLE AI_REPORT_DEF (
    REPORT_ID VARCHAR2(100) CONSTRAINT PK_AI_REPORT_DEF PRIMARY KEY,
    REPORT_NAME VARCHAR2(200) NOT NULL,
    DESCRIPTION CLOB,
    SQL_TEXT CLOB NOT NULL,
    CHART_CONFIG CLOB,
    LAYOUT_CONFIG CLOB,
    VARIABLES_CONFIG CLOB,
    STATUS NUMBER DEFAULT 100,  -- 100:草稿 200:已发布 300:已停用
    CREATED_BY VARCHAR2(100),
    CREATED_TIME DATE DEFAULT SYSDATE,
    UPDATED_TIME DATE,
    MENU_ID VARCHAR2(100)
);

-- 添加表注释
COMMENT ON TABLE AI_REPORT_DEF IS 'AI 报表定义表';
COMMENT ON COLUMN AI_REPORT_DEF.REPORT_ID IS '报表 ID';
COMMENT ON COLUMN AI_REPORT_DEF.REPORT_NAME IS '报表名称';
COMMENT ON COLUMN AI_REPORT_DEF.DESCRIPTION IS '报表描述';
COMMENT ON COLUMN AI_REPORT_DEF.SQL_TEXT IS 'SQL 语句';
COMMENT ON COLUMN AI_REPORT_DEF.CHART_CONFIG IS '图表配置 JSON';
COMMENT ON COLUMN AI_REPORT_DEF.LAYOUT_CONFIG IS '布局配置 JSON';
COMMENT ON COLUMN AI_REPORT_DEF.VARIABLES_CONFIG IS '变量配置 JSON';
COMMENT ON COLUMN AI_REPORT_DEF.STATUS IS '状态：100 草稿 200 已发布 300 已停用';
COMMENT ON COLUMN AI_REPORT_DEF.CREATED_BY IS '创建人';
COMMENT ON COLUMN AI_REPORT_DEF.CREATED_TIME IS '创建时间';
COMMENT ON COLUMN AI_REPORT_DEF.UPDATED_TIME IS '更新时间';
COMMENT ON COLUMN AI_REPORT_DEF.MENU_ID IS '关联菜单 ID';

-- 创建索引
CREATE INDEX IDX_REPORT_NAME ON AI_REPORT_DEF(REPORT_NAME);
CREATE INDEX IDX_REPORT_STATUS ON AI_REPORT_DEF(STATUS);
CREATE INDEX IDX_REPORT_MENU ON AI_REPORT_DEF(MENU_ID);
CREATE INDEX IDX_REPORT_CREATED_BY ON AI_REPORT_DEF(CREATED_BY);

-- 2. 报表菜单表
CREATE TABLE AI_REPORT_MENU (
    MENU_ID VARCHAR2(100) CONSTRAINT PK_AI_REPORT_MENU PRIMARY KEY,
    MENU_NAME VARCHAR2(200) NOT NULL,
    PARENT_ID VARCHAR2(100) DEFAULT '0',
    SORT_ORDER NUMBER DEFAULT 0,
    ICON VARCHAR2(100),
    STATUS NUMBER DEFAULT 100,
    CREATED_TIME DATE DEFAULT SYSDATE,
    CREATED_BY VARCHAR2(100)
);

-- 添加表注释
COMMENT ON TABLE AI_REPORT_MENU IS 'AI 报表菜单表';
COMMENT ON COLUMN AI_REPORT_MENU.MENU_ID IS '菜单 ID';
COMMENT ON COLUMN AI_REPORT_MENU.MENU_NAME IS '菜单名称';
COMMENT ON COLUMN AI_REPORT_MENU.PARENT_ID IS '父菜单 ID';
COMMENT ON COLUMN AI_REPORT_MENU.SORT_ORDER IS '排序';
COMMENT ON COLUMN AI_REPORT_MENU.ICON IS '图标';
COMMENT ON COLUMN AI_REPORT_MENU.STATUS IS '状态：100 启用 200 停用';

-- 创建索引
CREATE INDEX IDX_MENU_PARENT ON AI_REPORT_MENU(PARENT_ID);
CREATE INDEX IDX_MENU_SORT ON AI_REPORT_MENU(SORT_ORDER);

