-- 4. 报表版本历史表（可选）
CREATE TABLE AI_REPORT_VERSION (
    VERSION_ID VARCHAR2(100) CONSTRAINT PK_AI_REPORT_VERSION PRIMARY KEY,
    REPORT_ID VARCHAR2(100) NOT NULL,
    VERSION_NUMBER NUMBER NOT NULL,
    SNAPSHOT_JSON CLOB,
    CHANGE_DESCRIPTION VARCHAR2(500),
    CREATED_BY VARCHAR2(100),
    CREATED_TIME DATE DEFAULT SYSDATE
);

COMMENT ON TABLE AI_REPORT_VERSION IS 'AI 报表版本历史表';
COMMENT ON COLUMN AI_REPORT_VERSION.VERSION_ID IS '版本 ID';
COMMENT ON COLUMN AI_REPORT_VERSION.REPORT_ID IS '报表 ID';
COMMENT ON COLUMN AI_REPORT_VERSION.VERSION_NUMBER IS '版本号';
COMMENT ON COLUMN AI_REPORT_VERSION.SNAPSHOT_JSON IS '配置快照 JSON';
COMMENT ON COLUMN AI_REPORT_VERSION.CHANGE_DESCRIPTION IS '变更说明';

CREATE INDEX IDX_VERSION_REPORT ON AI_REPORT_VERSION(REPORT_ID);

